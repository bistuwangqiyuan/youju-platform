export function getPostById(id: string): CommunityPost | undefined {
  return communityPosts.find((p) => p.id === id);
}

export function getPostsByCategory(category: string): CommunityPost[] {
  if (category === "all") return communityPosts;
  return communityPosts.filter((p) => p.category === category);
}

export function getPostsByDestination(destinationId: string): CommunityPost[] {
  return communityPosts.filter((p) => p.destination_id === destinationId);
}

export function getFeaturedPosts(): CommunityPost[] {
  return [...communityPosts].sort((a, b) => b.likes_count - a.likes_count).slice(0, 6);
}
